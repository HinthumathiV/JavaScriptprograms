//Math.sqrt()---->	Returns the square root of a number.
let squareRoot = Math.sqrt(36);
console.log("Squareroot of 36 is "+ squareRoot);

//Math.pow()---->	Returns the power of a number.
let power=Math.pow(3,4);
console.log("power of 3,4 is "+ power);

//Math.random----> Returns the loating-point, pseudo-random number between 0 (inclusive) and 1 (exclusive).
let Random = Math.random();
console.log("Random number is " + Random);

//Math.min()---->	Returns the minimum value among  a specified value.
let Minimum = Math.min(12,-4,7,89,0);
console.log("Minimum number is " + Minimum);

//Math.max()---->	Returns the maximum value among  a specified value.
let Maximum = Math.max(12,-4,7,89,0);
console.log("Maximum number is " + Maximum);

//Math.round()---> function returns the number rounded to the nearest integer.
let Round = Math.round(6.89);
console .log("Round number is " + Round);

// Math.exp() ---> returns e raised to the given power. It is equivalent to ex in mathematics.
let value = Math.exp(4);
console.log("Exp number is " + value);